     void Ocultar(){
      
        if(rol.getSelectedItem().equals("Administrador")){
            Menu m = new Menu();
            m.setVisible(true);
            this.setVisible(false);
            m.show();
        }
        else{
            if(rol.getSelectedItem().equals("Caja")){
            Menu m = new Menu();
            m.setVisible(true);
            this.setVisible(false);
            
            m.Almacen.setVisible(false);
             
             m.Empleados.setVisible(false);
             m.Cliente.setVisible(true);
             m.Compras.setVisible(true);
             m.Proveedor.setVisible(false);
             m.Ventas.setVisible(false);
             
             
             
             m.show();
            }
            else{
                if(rol.getSelectedItem().equals("Facturacion")){
                Menu m = new Menu();
                m.setVisible(true);
            this.setVisible(false);
               // Factura,Cliente,Almacen,proveedor
                        
                
               m.Cliente.setVisible(true);
               m.Almacen.setVisible(true);
                 m.Proveedor.setVisible(true);
                 m.Empleados.setVisible(false);
                 m.Compras.setVisible(true);
                 m.Ventas.setVisible(false);
                 
                m.show();
                }
                else{
                if (rol.getSelectedItem().equals("Almacen")){
                Menu m = new Menu();
                m.setVisible(true);
            this.setVisible(false);
               
               m.Cliente.setVisible(false);
               m.Almacen.setVisible(true);
                 m.Proveedor.setVisible(true);
                 m.Empleados.setVisible(true);
                 m.Compras.setVisible(true);
                 m.Ventas.setVisible(false);
                m.show();
                }
                }}}}
    
